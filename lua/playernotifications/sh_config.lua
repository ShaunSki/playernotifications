--Player Joined Config
EnablePlayerJoinNotifications = true -- This will show notifications of people who join. To turn off use false. To turn on use true.

TagName = "[Sandbox] " -- Your servers tag name. (Call it whatever you want, you can even call it the gamemode tag like [Sandbox] or [TTT].
TagColor = Color( 0, 255, 0, 255 ) -- The Tag Color!

PlayerNameColor = Color( 255, 0, 0, 255 ) -- The player names color!
PlayerTextColor = Color( 255, 255, 255, 255 ) -- The player names text color!
PlayerJoinText = " has joined the server!" -- The text when a player joins!

PlayerJoinTimeDelay = 3 -- When a player joins should it instantly show they connected or have a delay? I added this because (The client is still loading, though the server counts as the player connected.)

--Player Left Config
EnablePlayerLeaveNotifications = true -- This will shows notifications of people who leave. To turn off use false. To turn on use true.

PlayerNameLeaveColor = Color( 255, 0, 0, 255 ) --  The player names color!
PlayerTextLeaveColor = Color( 255, 255, 255, 255 ) -- The player names text color!
PlayerJoinLeaveText = " has left the server!" -- The text when a player leaves!

